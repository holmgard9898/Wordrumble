export type AdventureGoal =
  | { type: 'find-words'; words: Record<GameLanguage, string[]>; minScore?: number }
  | { type: 'reach-score'; target: number }
  | { type: 'find-long-word'; minLength: number }
  | { type: 'survive-moves'; moves: number }
  /** Find a single word worth at least `target` points (used with mode 'oneword'). */
  | { type: 'best-word-score'; target: number }
  /** Destroy `count` asteroids by letting them fall to the bottom row. */
  | { type: 'destroy-asteroids'; count: number }
  /** Find thematic words; each one reveals the next letter of `hiddenWord`. */
  | { type: 'hidden-word'; thematicWords: Record<GameLanguage, string[]>; hiddenWord: Record<GameLanguage, string> }
  /** Find a single specific word (translated per language). Used for puzzle levels. */
  | { type: 'single-word'; word: Record<GameLanguage, string> }
  /** Two hidden words revealed letter by letter (hidden1 first, then hidden2) by finding thematic words. Player must form both on the board. */
  | { type: 'two-hidden-words'; thematicWords: Record<GameLanguage, string[]>; hiddenWord1: Record<GameLanguage, string>; hiddenWord2: Record<GameLanguage, string> };

/** Compact cell descriptor: { l: letter, c: color }. */
export interface PresetCell { l: string; c: BubbleColor }

export interface StoryCard {
  title: Record<GameLanguage, string>;
  body: Record<GameLanguage, string>;
}

export interface AdventureLevel {
  id: string;
  number: number;
  /** Which adventure map this level belongs to (1 = beach→sky, 2 = space). Defaults to 1. */
  map?: number;
  name: Record<GameLanguage, string>;
  intro: Record<GameLanguage, string>;
  background: GameBackground;
  goal: AdventureGoal;
  /** Game mode rules to use for this level. Defaults to classic. */
  mode?: GameMode;
  /** Show progress bar against goal (e.g. score target). */
  showProgressBar?: boolean;
  /** Optional shop item id to unlock when this level is completed */
  unlocksShopItem?: string;
  /** Position on the map as % of map width/height */
  mapPosition: { x: number; y: number };
  /** Connects (line drawn) to these other level ids */
  connectsTo: string[];
  icon: string;
  /** Override default moves (e.g. easier early levels) */
  maxMoves?: number;
  /** Number of free rockets the player gets at start (level 8 powerup). */
  freeRockets?: number;
  /** Antigravity twist: bubbles fall upward; new bubbles spawn from the bottom. */
  antigravity?: boolean;
  /** Asteroid twist: place immovable asteroids on rows 4 & 6 (alternating cols). */
  asteroids?: boolean;
  /** Satellite twist: immovable 2x2 satellite in center with a laser powerup. */
  satellite?: boolean;
  /** UFO twist: immovable UFOs on rows 4 & 6 (alternating cols), each swap the bubble below them every move. */
  ufos?: boolean;
  /** Collapsing cave: from move 5+, top tiles get replaced by immovable rocks each move. */
  collapsingCave?: boolean;
  /** Adventure 3+: smitta-mekanik (infection + ghosts). */
  infection?: boolean;
  /** Adventure 3-3: powerups som placeras vid start (ex: ['swapcolor','swapcolor','swapletter']). */
  startPowerups?: Array<'swapletter' | 'swapcolor'>;
  /** Fully pre-determined start grid per language. Overrides random generation. */
  presetGrid?: Partial<Record<GameLanguage, PresetCell[][]>>;
  /** Per-language max moves override (takes priority over `maxMoves`). */
  maxMovesByLang?: Partial<Record<GameLanguage, number>>;
  /** Story-style intro cards shown instead of (or in addition to) the mode tutorial. */
  storyIntro?: StoryCard[];
  /** When true, hide the generic mode tutorial steps and show only the storyIntro. */
  hideModeTutorial?: boolean;
  /** Final celebration popup ("vi tog luftballong hem") when this level is completed. */
  finalCelebration?: boolean;
}

